import { useEffect, useRef } from "react";
import gsap from "gsap";
import ScrollTrigger from "gsap/ScrollTrigger";

gsap.registerPlugin(ScrollTrigger);

const CompanyInfo = () => {
    const sectionsRef = useRef([]);

    useEffect(() => {
        // GSAP animations for each section
        sectionsRef.current.forEach((section) => {
            gsap.fromTo(
                section,
                { opacity: 0, y: 50 }, // Start position (faded and slightly lower)
                {
                    opacity: 1,
                    y: 0,
                    duration: 1,
                    ease: "power3.out",
                    scrollTrigger: {
                        trigger: section,
                        start: "top 80%", // Start animation when the section is 80% in view
                        toggleActions: "play none none reverse",
                    },
                }
            );
        });
    }, []);

    return (
        <section className="py-12 px-8">
            {/* Product Usage Section */}
            <div className="text-center mb-10" ref={(el) => (sectionsRef.current[0] = el)}>
                <h2 className="text-4xl font-extrabold mb-4">Product Usage</h2>
                <div className="flex justify-center items-center gap-6 flex-wrap">
                    {/* Add your product usage logos here */}
                </div>
            </div>

            {/* Our Infrastructure */}
            <div className="bg-gray-100 rounded-xl p-6 mb-8 shadow-lg" ref={(el) => (sectionsRef.current[1] = el)}>
                <div className="mb-10 flex justify-center">
                    <video
                        autoPlay
                        muted
                        loop
                        className="h-64 w-64"
                    >
                        <source src="Infrastructure.mp4" type="video/mp4" />
                    </video>
                </div>
                <h3 className="text-3xl font-bold mb-4">Our Infrastructure</h3>
                <p className="text-lg text-gray-700 mb-4">
                    We have a well-established infrastructure sprawling over an area of 22,500 Sq.Feet. We own a state-of-the-art
                    infrastructure that is equipped with the latest machinery and technology in our organization.
                </p>
                <button className="py-2 px-6 bg-gradient-to-r from-blue-400 to-blue-600 text-white rounded-full hover:bg-blue-700 transition">
                    Read More
                </button>
            </div>

            {/* Our Mission */}
            <div className="bg-gray-100 rounded-xl p-6 mb-8 shadow-lg" ref={(el) => (sectionsRef.current[2] = el)}>
                <div className="mb-10 flex justify-center">
                    <video
                        autoPlay
                        muted
                        loop
                        className="h-64 w-64"
                    >
                        <source src="mission.mp4" type="video/mp4" />
                    </video>
                </div>
                <h3 className="text-3xl font-bold mb-4">Our Mission</h3>
                <p className="text-lg text-gray-700 mb-4">
                    To build a successful business that would stand the rigors of time focused on manufacturing the highest quality
                    and supplying the most cost-effective products with the best service to the customers.
                </p>
                <button className="py-2 px-6 bg-gradient-to-r from-blue-400 to-blue-600 text-white rounded-full hover:bg-blue-700 transition">
                    Read More
                </button>
            </div>

            {/* Our Vision */}
            <div className="bg-gray-100 rounded-xl p-6 shadow-lg" ref={(el) => (sectionsRef.current[3] = el)}>
                <div className="mb-10 flex justify-center">
                    <video
                        autoPlay
                        muted
                        loop
                        className="h-64 w-64"
                    >
                        <source src="Vision.mp4" type="video/mp4" />
                    </video>
                </div>
                <h3 className="text-3xl font-bold mb-4">Our Vision</h3>
                <p className="text-lg text-gray-700 mb-4">
                    Our vision is to be one of the most trusted manufacturers in Irrigation Equipment like HDPE Pipes and Coils,
                    Sprinkler Irrigation Systems, HDPE Fittings, Sprinkler Pipes, and Fittings.
                </p>
                <button className="py-2 px-6 bg-gradient-to-r from-blue-400 to-blue-600 text-white rounded-full hover:bg-blue-700 transition">
                    Read More
                </button>
            </div>
        </section>
    );
};

export default CompanyInfo;
